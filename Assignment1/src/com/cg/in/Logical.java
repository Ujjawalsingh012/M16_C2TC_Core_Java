package com.cg.in;

public class Logical {

	public static void main(String[] args) {
		// AND OR NOT
		int a = 4;
		int b = 4;
		int c = 5;
		if ((a == 4) && (b == 4)) {
			System.out.println(" logical And Operator");
		}
		if ((a == 4) || (b == c)) {
			System.out.println("logical OR Operator");
		}

	}

}
